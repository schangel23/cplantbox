def main():
    parser = argparse.ArgumentParser(
        description='Phase 9: Diurnal Coupling Loop (Multi-Plant)',
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  # Mode A: Single day diurnal cycle (hourly timestep for faster test)
  python run_diurnal.py --days 55 --timestep-min 60

  # Mode A: Single day, 30-min resolution
  python run_diurnal.py --days 55 --timestep-min 30

  # Mode B: Multi-day growth series
  python run_diurnal.py --growth-days 20,35,55 --timestep-min 60

  # Mode A without Baleno (faster, uses Tair for Tleaf)
  python run_diurnal.py --days 55 --timestep-min 60 --no-baleno
        """,
    )

    mode = parser.add_mutually_exclusive_group(required=True)
    mode.add_argument('--days', type=int,
                      help='Single day: simulation day for diurnal cycle')
    mode.add_argument('--growth-days', type=str,
                      help='Growth series: comma-separated simulation days')

    parser.add_argument('--timestep-min', type=int, default=30,
                        help='Timestep in minutes (default: 30)')
    parser.add_argument('--no-baleno', action='store_true',
                        help='Skip Baleno energy balance (use Tair for Tleaf)')
    parser.add_argument('--met-csv', type=str, default=None,
                        help='Path to met forcing CSV')
    parser.add_argument('--skip-photosynthesis', action='store_true',
                        help='Only compute aPAR (skip photosynthesis)')
    parser.add_argument('--iterate-gs', action='store_true',
                        help='Enable iterative Tuzet-Baleno gs coupling (Phase 10)')
    parser.add_argument('--gs-max-iter', type=int, default=6,
                        help='Max gs coupling iterations (default: 6)')
    parser.add_argument('--gs-tolerance', type=float, default=0.05,
                        help='Relative gs convergence threshold (default: 0.05)')
    parser.add_argument('--gs-damping', type=float, default=0.6,
                        help='gs under-relaxation factor (default: 0.6)')

    # Production mode flags (Mode B with carbon + AgroC)
    parser.add_argument('--with-carbon', action='store_true',
                        help='Enable carbon partitioning + AgroC export per day')
    parser.add_argument('--with-agroc', action='store_true',
                        help='Run AgroC Fortran after all days complete')
    parser.add_argument('--carbon-method', type=str, default='auto',
                        choices=['auto', 'phloem', 'dvs'],
                        help='Carbon partitioning method (default: auto)')
    parser.add_argument('--carbon-solver', type=str, default='s5',
                        choices=['s5', 'pm'],
                        help="Carbon partitioning solver (Gate Ch1.PM.4): "
                             "'s5' (default — quasi-steady phloem solver, "
                             "bit-identical with pre-Gate-4 production) or "
                             "'pm' (PiafMunch substep loop mirroring "
                             "pm_notebook_loop.run_loop). PM mode adds "
                             "Q_S_Mesophyll mass-balance instrumentation to "
                             "per-plant outputs.")
    parser.add_argument('--resume', action='store_true',
                        help='Resume interrupted production run from checkpoint')
    parser.add_argument('--uniform', action='store_true',
                        help='Uniform baseline: skip DART/Baleno, '
                             'use clearsky PAR + Tair for all segments')
    parser.add_argument('--growth-mode', type=str, default='parametric',
                        choices=['parametric', 'carbon'],
                        help='Growth mode: parametric (default) or '
                             'carbon-limited feedback')

    # SIF fluorescence flags
    parser.add_argument('--with-sif', action='store_true',
                        help='Enable SIF fluorescence computation + per-segment output')
    parser.add_argument('--sif-triangles', action='store_true',
                        help='Write per-triangle SIF CSVs (large, requires --with-sif)')
    parser.add_argument('--with-dart-f', action='store_true',
                        help='Run DART fluorescence RT for TOC SIF radiance '
                             '(requires --with-sif)')

    # Soil-water provider (Phase 2 of DuMux integration)
    parser.add_argument('--soil-mode', type=str, default='fixed',
                        choices=['fixed', 'bucket', 'dumux'],
                        help="Soil water provider: 'fixed' (legacy uniform "
                             "psi, default — bit-identical with prior runs), "
                             "'bucket' (lumped exponential drying fallback), "
                             "'dumux' (1D Richards via rosi_richards.so)")
    parser.add_argument('--soil-psi-cm', type=float, default=-500.0,
                        help='Initial soil pressure head [cm] (default -500). '
                             'Used as initial condition by all soil-mode '
                             'providers')

    # Site and FLUXNET forcing
    parser.add_argument('--site', type=str, default=None,
                        help='Site name (e.g. us-ne1). Overrides location/sowing defaults.')
    parser.add_argument('--fluxnet-csv', type=str, default=None,
                        help='Path to FLUXNET FULLSET hourly CSV file')
    parser.add_argument('--fluxnet-year', type=int, default=None,
                        help='Year to extract from FLUXNET CSV')

    args = parser.parse_args()

    # Apply site config if specified
    if args.site:
        import os
        os.environ['COUPLING_SITE'] = args.site.lower()
        from ..config import get_site
        site = get_site(args.site.lower())
        global LAT, LON, SOWING_DATE, DEFAULT_CO2_PPM
        LAT = site['lat']
        LON = site['lon']
        SOWING_DATE = site['sowing_date']
        DEFAULT_CO2_PPM = site['co2_ppm']
        print(f"  Site: {args.site} ({LAT:.3f}°N, {LON:.3f}°E, "
              f"sowing={SOWING_DATE}, CO2={DEFAULT_CO2_PPM} ppm)")

    # Convert FLUXNET CSV to pipeline met CSV if provided
    if args.fluxnet_csv:
        if args.fluxnet_year is None:
            raise ValueError("--fluxnet-year required with --fluxnet-csv")
        from ..utils.met_forcing import fluxnet_to_pipeline_csv, inject_fluxnet_into_dvs
        from ..config import OUTPUT_DIR
        # Write converted CSV to output dir
        met_csv_path = str(OUTPUT_DIR / f'fluxnet_{args.fluxnet_year}_met.csv')
        fluxnet_to_pipeline_csv(args.fluxnet_csv, args.fluxnet_year, met_csv_path)
        inject_fluxnet_into_dvs(args.fluxnet_csv, args.fluxnet_year, SOWING_DATE)
        args.met_csv = met_csv_path

    print("Phase 9: Time-Series Diurnal Coupling Loop (Multi-Plant)")
    print("=" * 70)

    enable_baleno = not args.no_baleno

    # Build soil-ψ provider once and thread through all invocation paths.
    # 'fixed' is bit-identical with the legacy hardcoded -500 cm path.
    #
    # Grid spec: the provider's rsx vector is indexed by the same cellidx
    # that ``MappedSegments::setRectangularGrid`` assigns to each segment
    # inside ``grow_plant``. Both sides must agree on
    # ``prod(cell_number)`` or ``hm.solve`` raises an OOB range-check
    # (``__n=N >= size=M``) at the first PM substep. We import the
    # ``DEFAULT_SOIL_*`` triplet from ``growth/grow.py`` so the two
    # sides stay locked together.
    from ..hydraulics.soil_psi import make_provider, make_provider_pool
    from ..growth.grow import (
        DEFAULT_SOIL_MIN_B, DEFAULT_SOIL_MAX_B, DEFAULT_SOIL_CELL_NUMBER,
    )

    _n_cells_total = int(
        DEFAULT_SOIL_CELL_NUMBER[0]
        * DEFAULT_SOIL_CELL_NUMBER[1]
        * DEFAULT_SOIL_CELL_NUMBER[2]
    )
    if args.soil_mode == "dumux":
        _grid_kwargs = dict(
            min_b=DEFAULT_SOIL_MIN_B,
            max_b=DEFAULT_SOIL_MAX_B,
            cell_number=DEFAULT_SOIL_CELL_NUMBER,
        )
    else:
        # FixedSoilPsi / BucketSoilPsi take a flat ``n_cells`` scalar.
        _grid_kwargs = dict(n_cells=_n_cells_total)

    soil_psi_provider = make_provider(args.soil_mode,
                                      soil_psi_cm=args.soil_psi_cm,
                                      **_grid_kwargs)
    print(f"  soil_mode={args.soil_mode}  psi_init={args.soil_psi_cm} cm  "
          f"n_cells_total={_n_cells_total}")

    # Per-plant soil-ψ pool (Gate Ch1.PMDM.5). Only built when
    # ``--carbon-solver=pm`` because the PM dispatch is the only path
    # that pushes RWU sinks back to the provider; under S5, the read-only
    # ``soil_psi_provider`` shared across all plants is sufficient. For
    # ``dumux`` mode the pool entries are independent ``RichardsSP``
    # instances (~few seconds each to construct, total pool build ≈
    # 30-90 s for N_PLANTS=9). For ``fixed`` / ``bucket`` the pool is
    # a list of cheap stateless objects.
    soil_psi_pool = None
    if args.carbon_solver == 'pm':
        print(f"  Building per-plant soil-ψ pool "
              f"(N_PLANTS={N_PLANTS}, mode={args.soil_mode})...")
        soil_psi_pool = make_provider_pool(
            args.soil_mode, n_plants=N_PLANTS,
            soil_psi_cm=args.soil_psi_cm,
            **_grid_kwargs,
        )

    if args.uniform:
        # Uniform baseline: skip DART/Baleno, use clearsky PAR + Tair
        if args.days is not None:
            result = run_single_day(
                args.days, use_dart=False,
                timestep_min=args.timestep_min,
                met_csv=args.met_csv,
                soil_psi_provider=soil_psi_provider,
            )
        else:
            growth_days = [int(d.strip()) for d in args.growth_days.split(',')]
            if args.with_carbon:
                result = run_production_series(
                    growth_days, use_dart=False,
                    timestep_min=args.timestep_min,
                    carbon_method=args.carbon_method,
                    run_agroc_fortran=args.with_agroc,
                    resume=args.resume,
                    with_sif=args.with_sif, with_dart_f=args.with_dart_f,
                    sif_triangles=args.sif_triangles,
                    soil_psi_provider=soil_psi_provider,
                    soil_psi_pool=soil_psi_pool,
                    carbon_solver=args.carbon_solver,
                )
            else:
                raise ValueError(
                    "Use --with-carbon for multi-day uniform series")
    elif args.days is not None:
        # Mode A: single day
        result = run_single_day(
            args.days, use_dart=True,
            timestep_min=args.timestep_min,
            enable_baleno=enable_baleno,
            met_csv=args.met_csv,
            skip_photosynthesis=args.skip_photosynthesis,
            iterate_gs=args.iterate_gs,
            gs_max_iterations=args.gs_max_iter,
            gs_tolerance=args.gs_tolerance,
            gs_damping_alpha=args.gs_damping,
            with_sif=args.with_sif, with_dart_f=args.with_dart_f,
            sif_triangles=args.sif_triangles,
            soil_psi_provider=soil_psi_provider,
        )
    else:
        # Mode B: growth series
        growth_days = [int(d.strip()) for d in args.growth_days.split(',')]
        if args.growth_mode == 'carbon' and args.with_carbon:
            # Carbon-feedback growth mode
            result = run_production_series_carbon(
                growth_days,
                timestep_min=args.timestep_min,
                enable_baleno=enable_baleno,
                iterate_gs=args.iterate_gs,
                gs_max_iterations=args.gs_max_iter,
                gs_tolerance=args.gs_tolerance,
                gs_damping_alpha=args.gs_damping,
                carbon_method=args.carbon_method,
                run_agroc_fortran=args.with_agroc,
                resume=args.resume,
                with_sif=args.with_sif, with_dart_f=args.with_dart_f,
                sif_triangles=args.sif_triangles,
                soil_psi_provider=soil_psi_provider,
                soil_psi_pool=soil_psi_pool,
                carbon_solver=args.carbon_solver,
            )
        elif args.with_carbon:
            # Production mode: full carbon + AgroC pipeline (parametric growth)
            result = run_production_series(
                growth_days, use_dart=True,
                timestep_min=args.timestep_min,
                enable_baleno=enable_baleno,
                iterate_gs=args.iterate_gs,
                gs_max_iterations=args.gs_max_iter,
                gs_tolerance=args.gs_tolerance,
                gs_damping_alpha=args.gs_damping,
                carbon_method=args.carbon_method,
                run_agroc_fortran=args.with_agroc,
                resume=args.resume,
                with_sif=args.with_sif, with_dart_f=args.with_dart_f,
                sif_triangles=args.sif_triangles,
                soil_psi_provider=soil_psi_provider,
                soil_psi_pool=soil_psi_pool,
                carbon_solver=args.carbon_solver,
            )
        else:
            raise ValueError(
                "Use --with-carbon for multi-day growth series")


if __name__ == '__main__':
    main()
