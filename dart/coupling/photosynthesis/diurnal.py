def _diagnose_ori_missing(simu, dart_obj_paths):
    """Print actionable diagnostic when ``read_ori_reindex_multi`` returns
    None (no ``.ori`` reindex tables generated by DART maket).

    The pipeline previously zero-filled ``daily_An_mol_per_plant`` and
    silently continued — fine in isolation but catastrophic when daily
    summaries get loaded from checkpoints: the zero-An summaries hide
    the upstream failure indefinitely. This helper surfaces:

      * whether the simulation's ``input/triangles/`` directory exists
        and what's in it (helps distinguish "maket never ran" from
        "maket ran but stripped everything");
      * vertex/face/group counts for the first few plant OBJs (catches
        malformed mesh exports — e.g. PM-CW_Gr-clamped near-zero
        leaves writing zero-area triangles).

    Caller decides whether to ``raise`` or fall through to legacy
    zero-fill via the ``COUPLING_TOLERATE_ORI_MISSING`` env var.

    Reference: 2026-05-09 Gate Ch1.PMDM.5 server-side diagnosis (the
    .ori bug was masked for hours because failed runs loaded the
    previous checkpoint's An values and looked correct from the
    outside).
    """
    from pathlib import Path as _Path
    print(f"  ERROR: No .ori reindex tables!")
    simu_path = _Path(str(simu.simu_dir))
    tri_dir = simu_path / 'input' / 'triangles'
    print(f"    triangles dir: {tri_dir}")
    print(f"    exists: {tri_dir.exists()}")
    if tri_dir.exists():
        files = sorted(tri_dir.iterdir())
        print(f"    contents ({len(files)} files): "
              f"{[f.name for f in files[:10]]}")
    for pi, obj_path in enumerate(dart_obj_paths[:3]):
        n_v = n_f = n_g = 0
        try:
            with open(obj_path) as f:
                for line in f:
                    if line.startswith('v '):
                        n_v += 1
                    elif line.startswith('f '):
                        n_f += 1
                    elif line.startswith('g '):
                        n_g += 1
            print(f"    plant {pi} OBJ {_Path(obj_path).name}: "
                  f"{n_v} verts, {n_f} faces, {n_g} groups")
        except Exception as e:
            print(f"    plant {pi} OBJ {obj_path}: read error {e}")
    if os.environ.get("COUPLING_TOLERATE_ORI_MISSING", "0").lower() in (
            "1", "on", "true"):
        print(f"    COUPLING_TOLERATE_ORI_MISSING=1 set, falling "
              f"through to zero-An (legacy behaviour)")
        return
    raise RuntimeError(
        f"DART produced no .ori reindex tables for {simu_path}. "
        f"This is the upstream bug logged in Gate Ch1.PMDM.5 server-"
        f"side acceptance — DART maket completes without writing the "
        f"per-group .ori files that the photosynthesis pipeline "
        f"needs. Investigate the simulation's input/triangles/ dir "
        f"and the OBJ vert/face counts above. Set "
        f"COUPLING_TOLERATE_ORI_MISSING=1 to fall through to the "
        f"legacy zero-fill behaviour (NOT recommended — checkpoint "
        f"reloads will hide the failure)."
    )


def _real_met_kwargs(sim_day):
    """Get diurnal_met_profile kwargs from real daily met data for sim_day.

    Returns empty dict if no data available (uses default sinusoidal).
    """
    from ..carbon.dvs_partitioning import get_daily_met
    day_met = get_daily_met(sim_day)
    if day_met is None:
        return {}
    kwargs = {
        'T_min': day_met['T_min_C'],
        'T_max': day_met['T_max_C'],
    }
    if 'RH_min' in day_met:
        kwargs['RH_min'] = day_met['RH_min']
        kwargs['RH_max'] = day_met['RH_max']
    if 'wind_mean_ms' in day_met:
        kwargs['wind_min'] = day_met['wind_mean_ms'] * 0.5
        kwargs['wind_max'] = day_met['wind_max_ms'] / 3.0
    return kwargs


